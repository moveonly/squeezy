use std::fs;

use crate::{
    report::{GrepBaselineMode, GrepBaselineSpec, QueryBaselineStatus, QuerySpec},
    util::temp_dir,
};

use super::*;

fn empty_query(id: &str, kind: &str) -> QuerySpec {
    QuerySpec {
        id: id.to_string(),
        kind: kind.to_string(),
        text: String::new(),
        symbol_kind: None,
        owner_kind: None,
        attribute: None,
        from: None,
        to: None,
        expected_contains: Vec::new(),
        documented_misses: Vec::new(),
        baseline: None,
    }
}

#[test]
fn fallback_quality_query_reports_generated_and_vendor_paths() {
    let root = temp_dir("fallback-quality-query").unwrap();
    fs::create_dir_all(root.join("src")).unwrap();
    fs::create_dir_all(root.join("vendor")).unwrap();
    fs::write(
        root.join("Cargo.toml"),
        "[package]\nname = \"fallback_quality_query\"\nversion = \"0.1.0\"\nedition = \"2024\"\n",
    )
    .unwrap();
    fs::write(root.join("src/lib.rs"), "pub fn local() {}\n").unwrap();
    fs::write(
        root.join("src/generated.rs"),
        "// @generated by benchmark test\npub fn generated() {}\n",
    )
    .unwrap();
    fs::write(root.join("vendor/lib.rs"), "pub fn vendored() {}\n").unwrap();

    let build = build_graph(&root).unwrap();
    let fallback = fallback_quality_report(
        &build.coverage,
        build.unsupported_files,
        build.unsupported_file_samples.clone(),
        &build.graph,
    );
    let mut query = empty_query("fallback-quality", "fallback_quality");
    query.expected_contains = vec!["generated".to_string(), "vendor".to_string()];

    let report = run_query(&build.snapshot, &build.graph, &query, &fallback).unwrap();

    assert!(report.missing.is_empty(), "{:?}", report);
    assert!(report.actual.contains(&"generated".to_string()));
    assert!(report.actual.contains(&"vendor".to_string()));
}

#[test]
fn baseline_path_matches_directory_prefix() {
    assert!(baseline_path_matches("src/", "src/lib.rs"));
    assert!(baseline_path_matches("src", "src/lib.rs"));
    assert!(baseline_path_matches("src", "src"));
    assert!(baseline_path_matches("/src/", "src/lib.rs"));
    assert!(baseline_path_matches("src/", "src/nested/mod.rs"));
    assert!(!baseline_path_matches("src/", "other/src/lib.rs"));
    assert!(!baseline_path_matches("src/", "tests/lib.rs"));
    assert!(baseline_path_matches("*.rs", "src/lib.rs"));
    assert!(!baseline_path_matches("*.rs", "src/lib.ts"));
    assert!(baseline_path_matches("*", "anything"));
}

#[test]
fn grep_baseline_uses_cached_snapshot_with_include() {
    let root = temp_dir("grep-baseline-include").unwrap();
    fs::create_dir_all(root.join("src")).unwrap();
    fs::create_dir_all(root.join("tests")).unwrap();
    fs::write(
        root.join("Cargo.toml"),
        "[package]\nname = \"grep_baseline_include\"\nversion = \"0.1.0\"\nedition = \"2024\"\n",
    )
    .unwrap();
    fs::write(root.join("src/lib.rs"), "pub fn target() {}\n").unwrap();
    fs::write(root.join("tests/it.rs"), "fn target() {}\n").unwrap();

    let build = build_graph(&root).unwrap();
    let fallback = fallback_quality_report(
        &build.coverage,
        build.unsupported_files,
        build.unsupported_file_samples.clone(),
        &build.graph,
    );
    let mut query = empty_query("ref", "reference_search");
    query.text = "target".to_string();
    query.baseline = Some(GrepBaselineSpec {
        pattern: Some("target".to_string()),
        include: vec!["src/".to_string()],
        mode: GrepBaselineMode::Paths,
        unsupported_reason: None,
    });
    let report = run_query(&build.snapshot, &build.graph, &query, &fallback).unwrap();
    assert_eq!(report.baseline.status, QueryBaselineStatus::Ran);
    assert_eq!(report.baseline.actual, vec!["src/lib.rs".to_string()]);
    assert_eq!(report.baseline.files_scanned, 1);

    let mut unsupported = empty_query("u", "reference_search");
    unsupported.text = "target".to_string();
    unsupported.baseline = Some(GrepBaselineSpec {
        pattern: None,
        include: Vec::new(),
        mode: GrepBaselineMode::Unsupported,
        unsupported_reason: Some("not modelable".to_string()),
    });
    let unsupported_report =
        run_query(&build.snapshot, &build.graph, &unsupported, &fallback).unwrap();
    assert_eq!(
        unsupported_report.baseline.status,
        QueryBaselineStatus::Unsupported
    );
}
